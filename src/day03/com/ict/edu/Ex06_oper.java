package day03.com.ict.edu;

public class Ex06_oper {
	public static void main(String[] args) {
		// 대입연산 : =, +=, -=,*=, /=, %=
		// A = B -> 변수 A에 데이터 B를 저장
		// A += B -> A의 값과 B의 값을 더한 후 A에 저장

		int su1 = 7;

		su1 += 4; // su1 = su1 + 4;
		System.out.println("su1 : " + su1);
		
		su1 -= 4; // su1 = su1 - 4;
		System.out.println("su1 : " + su1);
		
		
	}
}
