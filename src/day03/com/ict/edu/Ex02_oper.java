package day03.com.ict.edu;

public class Ex02_oper {

	public static void main(String[] args) {
		// 증감연산자 : 피연산자의 값을 1씩 증가 혹은 감소를 시킨다.
		int su1 = 10;
		int su2 = 10;
		
		System.out.println(++su1);
		System.out.println(su2++);
		System.out.println(su1);
		System.out.println(su2);
		
		char c1 = 'b';
		char c2 = 'B';
		
		System.out.println(++c1);
		System.out.println(c2++);
		System.out.println(c1);
		System.out.println(c2);
	}

}
