package day02.com.ict.edu;
class Ex01{
	public static void main(String[] args){
	
		// 논리 자료형을 선언, 할당
		boolean b1;
		b1 = true;
		
		// b1을 호출해보자
		// **이름을 호출하면 데이터가 나온다
		System.out.println(b1);
		
		// b1의 상태를 false로 변경한다.
		b1 = false;
		System.out.println(b1);
		
		// b2를 선언과 대입을 한줄에!
		boolean b2 = true;
		System.out.println(b2);
		
		// b2를 false로 변경!
		b2 = false;
		System.out.println(b2);
	
	}
}